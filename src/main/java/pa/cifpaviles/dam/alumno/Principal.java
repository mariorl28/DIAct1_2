    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       int edad=Integer.parseInt(txtEdad.getText());
        Alumno alumno1 =new Alumno(txtNombre.getText(),txtApellidos.getText(),edad,txtEmail.getText(),txtTelefono.getName());
        
        
        if(contador > 5){
            lblLleno.setVisible(true);
            
        
    }
        else{
            alumnos.add(alumno1);
            lblMenor.setText(alumno1.getNmenor());
        }
        
        contador++;
        txtNombre.setText("");txtApellidos.setText("");txtEdad.setText("");txtEmail.setText("");txtTelefono.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

